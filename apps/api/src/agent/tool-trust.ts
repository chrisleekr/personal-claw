/**
 * Fast lookup map built from `TOOL_TRUST_REGISTRY`. Exported for the
 * self-test to enumerate; consumers should prefer `getToolTrustCategory()`.
 */
export const TOOL_TRUST_MAP: ReadonlyMap<string, ToolTrustEntry> = new Map(
  TOOL_TRUST_REGISTRY.map((entry) => [entry.toolName, entry]),
);

/**
 * Resolves a tool name to its trust category. Returns `external_untrusted`
 * for any tool not explicitly registered, per FR-030's fail-closed default.
 *
 * MCP tools are always routed through this function with the fallback behavior:
 * they are unknown to the static registry and therefore default to
 * `external_untrusted`, which can be overridden per-channel via the
 * `trust_mcp_tool` entries in `detection_overrides`.
 *
 * @param toolName The tool's registered name as seen by the agent
 * @returns The trust category to apply to the tool's output
 */
export function getToolTrustCategory(toolName: string): ToolTrustCategory {
  return TOOL_TRUST_MAP.get(toolName)?.category ?? 'external_untrusted';
}

/**
 * Enumerates the full set of statically-registered tool names that should
 * appear in `TOOL_TRUST_REGISTRY`. The self-test uses this to detect new
 * tools that were added without a trust decision.
 */
export function getRegisteredToolNames(): readonly string[] {
  return TOOL_TRUST_REGISTRY.map((entry) => entry.toolName);
}
