/**
 * Tiered tool-output trust registry for the multi-layer injection detection
 * pipeline (FR-030, FR-031).
 *
 * Every statically-registered tool in this repository MUST have an explicit
 * entry in `TOOL_TRUST_REGISTRY`. A companion self-test in
 * `apps/api/src/agent/__tests__/tool-trust.test.ts` enforces this by booting
 * a `ToolRegistry` with every `ToolProvider` registered, collecting all tool
 * names, and asserting each one appears below.
 *
 * **Any new tool added after this feature ships defaults to
 * `external_untrusted`** (Category 3) if it is missing from this registry.
 * The self-test will fail until a deliberate entry is added with an inline
 * justification. This is by design — it forces a security decision for every
 * new tool before merge.
 *
 * Categories (full spec in `specs/.../spec.md` FR-030):
 * - `system_generated` — no attacker path; bypass detection
 * - `already_detected` — already filtered by an earlier pipeline pass; bypass detection
 * - `external_untrusted` — direct attacker path; MUST pass every input-side layer
 * - `mixed` — blend of system and attacker-reachable fields; untrusted by default,
 *   with optional opt-in trust at subcommand/path granularity
 *
 * MCP tools are not listed here because their names are dynamic and channel-scoped.
 * Per research R9, MCP tools default to `external_untrusted` via the fallback
 * logic in `approval-gateway.wrapTools()` and admins can mark specific MCP
 * tool names as trusted via a per-channel `trust_mcp_tool` override in the
 * `detection_overrides` table.
 */

export type ToolTrustCategory =
  | 'system_generated'
  | 'already_detected'
  | 'external_untrusted'
  | 'mixed';

/**
 * Per-subcommand opt-in trust pattern for Category 4 (mixed) tools.
 * The pattern is tested against the tool's invocation arguments; a match
 * promotes that specific invocation from `mixed` to `system_generated` for
 * detection purposes.
 */
export interface TrustedSubcommandPattern {
  pattern: RegExp;
  justification: string;
}

export interface ToolTrustEntry {
  toolName: string;
  category: ToolTrustCategory;
  justification: string;
  trustedSubcommandPatterns?: readonly TrustedSubcommandPattern[];
}

